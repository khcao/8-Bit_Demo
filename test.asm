sprite_line: ;;; Draw 8 pixel horizontal from DE to HL
	ld (spointer), sp   ;;; store SP
	
	ld ixl, e
	ld ixh, d
	ld sp, ix	;;;Load DE into SP
	
	pop de
	ld (hl), e
	inc l
	ld (hl), d
	inc l
	pop de
	ld (hl), e
	inc l
	ld (hl), d
	inc l
	pop de
	ld (hl), e
	inc l
	ld (hl), d
	inc l
	pop de
	ld (hl), e
	inc l
	ld (hl), d
	inc l
	ld a, $e0	
	and l
	ld iy, x_axis
	ld l, (iy+0)
	or l
	ld l, a	;;; Restore x-axis
	
	ld ix, $0000
	add ix, sp
	ld d, ixh
	ld e, ixl ;;; Restore DE
	
	ld sp,(spointer)  ;;; Restore SP
	ret   ;;; sprite_line ret

x_axis:
	defb $00
draw_sprite: ;;; Draw 64x64 sprite from location DE to HL (no OOB handle)
	ld a, $1F
	and l
	ld (x_axis), a
	ld c, 64
loop_line:
	ld a, h
	or $F8
	add a, 1
	
	jp c, next_att
	nop
	
	inc h		;;; Next line in same attribute
	jp print_line
next_att:       ;;; Fixes HL for next attribute
	ld a, $F8
	and h
	ld h, a
	
	ld a, $20
	add a, l
	ld l, a
	
print_line:
	call sprite_line
	nop

	dec c
	jp nz, loop_line
	nop
	
	ret ;;; Draw sprite ret
	
